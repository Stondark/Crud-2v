function info_ajax(){
    $.ajax({
        type: 'POST',
        url: "../controller/dashboard_controller.php",
        dataType: "json",
    }).done(function(res){
            //var datos = res;
            const cop_money = new Intl.NumberFormat("es-ES",{
                style: "currency",
                currency: "COP",
                minimumFractionDigits: 0
            });
            document.getElementById("prod_inv").innerHTML = res.count_prod;
            document.getElementById("total_ventas").innerHTML = res.count_ventas;
            document.getElementById("money_ventas").innerHTML = cop_money.format(res.total_ventas["SUM(`total`)"]);
    })
}

